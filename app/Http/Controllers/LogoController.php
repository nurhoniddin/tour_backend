     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Logo  $logo
     * @return \Illuminate\Http\Response
     */
    public function destroy(Logo $logo)
    {
	    Storage::disk('public')->delete($logo->logo);
	    $logo->delete();
